"""
Live Benchmark Tracker
======================
Scores every real user query that passes through the Fintex pipeline and
persists results to the Supabase `benchmark_results` table.

Runs entirely in a background thread — zero latency impact on user responses.

Supabase setup (run once):
    CREATE TABLE IF NOT EXISTS public.benchmark_results (
      id              bigint generated by default as identity primary key,
      query_id        text not null,
      session_id      text,
      user_id         text,
      query           text not null,
      category        text,
      subcategory     text,
      is_prediction   boolean default false,
      actual_route    text,
      answer_preview  text,
      response_time_ms float,
      route_score     int default 0,
      verdict_score   int default 0,
      no_hedge_score  int default 0,
      format_score    int default 0,
      pakistan_score  int default 0,
      total_score     int default 0,
      passed          boolean default false,
      source_label    text,
      accuracy_min    int,
      accuracy_max    int,
      created_at      timestamp with time zone
                        default timezone('utc'::text, now()) not null
    );
    CREATE INDEX IF NOT EXISTS idx_benchmark_created ON public.benchmark_results (created_at DESC);
    CREATE INDEX IF NOT EXISTS idx_benchmark_category ON public.benchmark_results (category);
"""

from __future__ import annotations

import re
import uuid
import threading
from datetime import datetime, timezone
from typing import Any, Dict, List, Optional

import sys
import os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), '..', '..'))


# ─────────────────────────────────────────────────────────────────────────────
# SCORING CONSTANTS  (mirrors tests/finance_benchmark.py)
# ─────────────────────────────────────────────────────────────────────────────

VERDICT_PATTERNS = [
    r"BULLISH", r"BEARISH", r"NEUTRAL",
    r"BETTER\s*\(Bullish\)", r"WORSE\s*\(Bearish\)",
    r"\*\*Verdict:\*\*",
    r"BETTER \(Bullish\)", r"WORSE \(Bearish\)",
]

FORBIDDEN_PREDICTION_PHRASES = [
    "cannot be determined",
    "data is unavailable",
    "no technical indicators",
    "cannot predict",
    "inherently speculative",
    "lack of technical data",
    "absence of specific technical indicators",
    "prediction is impossible",
    "cannot make a forecast",
    "not possible to predict",
]

PREDICTION_KEYWORDS = [
    "predict", "prediction", "forecast", "will it", "going to", "gonna",
    "will be", "future price", "target price", "price target",
    "better or bad", "perform on", "perform in", "buy or sell",
    "should i buy", "should i sell", "worth buying", "good time to buy",
    "tomorrow", "next week", "next month",
]

# Required sections per category — used to score format compliance
REQUIRED_SECTIONS: Dict[str, List[str]] = {
    "stocks_prediction": ["Technical Position", "Signal Breakdown", "Prediction", "Key Risks"],
    "stocks":            ["Company Background", "Performance Analysis", "Fintex"],
    "theory":            ["Definition", "Key Points"],
    "macro":             ["Pakistan", "percent"],
    "monetary_policy":   ["SBP", "rate"],
    "banking":           ["bank"],
}

# Words that indicate Pakistan-relevant financial content
PAKISTAN_SIGNALS = [
    "pkr", "psx", "sbp", "kse", "pakistan", "karachi",
    "lahore", "islamabad", "rupee", "kibor",
]

VALID_CATEGORIES = {
    "stocks", "macro", "theory", "banking",
    "monetary_policy", "general", "off_topic",
}


# ─────────────────────────────────────────────────────────────────────────────
# SCORER
# ─────────────────────────────────────────────────────────────────────────────

def _score_response(pipeline_result: Dict[str, Any]) -> Dict[str, Any]:
    """
    Rule-based scorer — no LLM calls, runs in microseconds.

    Returns a dict of individual scores (each 0 or 20) plus total (0–100).
    """
    answer_obj = pipeline_result.get("answer", {})
    answer_text = (
        answer_obj.get("answer", "")
        if isinstance(answer_obj, dict)
        else str(answer_obj)
    )
    answer_lower = answer_text.lower()
    category = pipeline_result.get("category", "unknown")
    is_prediction = pipeline_result.get("is_prediction", False)
    query = pipeline_result.get("query", "").lower()

    scores: Dict[str, int] = {}

    # ── Route score (20 pts): category is a known valid value ──────────────
    scores["route_score"] = 20 if category in VALID_CATEGORIES else 0

    # ── Verdict score (20 pts): prediction queries must have a direction ───
    is_pred_query = is_prediction or any(kw in query for kw in PREDICTION_KEYWORDS)
    if is_pred_query:
        has_verdict = any(
            re.search(p, answer_text, re.IGNORECASE) for p in VERDICT_PATTERNS
        )
        scores["verdict_score"] = 20 if has_verdict else 0
    else:
        scores["verdict_score"] = 20  # not applicable

    # ── No-hedge score (20 pts): forbidden phrases absent ──────────────────
    if is_pred_query:
        hedge_found = any(p in answer_lower for p in FORBIDDEN_PREDICTION_PHRASES)
        scores["no_hedge_score"] = 0 if hedge_found else 20
    else:
        scores["no_hedge_score"] = 20  # not applicable

    # ── Format score (20 pts): required markdown sections present ──────────
    if is_pred_query and category == "stocks":
        section_key = "stocks_prediction"
    else:
        section_key = category if category in REQUIRED_SECTIONS else None

    if section_key:
        required = REQUIRED_SECTIONS[section_key]
        hits = sum(1 for s in required if s.lower() in answer_lower)
        scores["format_score"] = round((hits / len(required)) * 20)
    else:
        scores["format_score"] = 20

    # ── Pakistan relevance score (20 pts): answer mentions Pakistan finance ─
    # Off-topic answers are exempt (they correctly say nothing about finance)
    if category == "off_topic":
        scores["pakistan_score"] = 20
    else:
        pak_hits = sum(1 for w in PAKISTAN_SIGNALS if w in answer_lower)
        scores["pakistan_score"] = 20 if pak_hits >= 1 else 0

    total = sum(scores.values())
    scores["total_score"] = total
    scores["passed"] = total >= 60

    return scores


# ─────────────────────────────────────────────────────────────────────────────
# LIVE BENCHMARK TRACKER
# ─────────────────────────────────────────────────────────────────────────────

class LiveBenchmarkTracker:
    """
    Wraps any pipeline.answer() call: scores the result and persists it to
    Supabase `benchmark_results` in a background thread.

    Usage (already wired into FintexPipeline.answer()):
        tracker = LiveBenchmarkTracker()
        tracker.track(pipeline_result, response_time_ms=elapsed)
    """

    def __init__(self):
        self._supabase = None
        self._table = "benchmark_results"
        self._lock = threading.Lock()
        self._enabled = True
        self._init_supabase()

    def _init_supabase(self) -> None:
        try:
            from src.db.connection import get_supabase_client
            self._supabase = get_supabase_client()
        except Exception as e:
            print(f"[Benchmark] Supabase init failed — tracking disabled: {e}")
            self._enabled = False

    # ─────────────────────────────────────────────────────────────────────
    # PUBLIC API
    # ─────────────────────────────────────────────────────────────────────

    def track(
        self,
        pipeline_result: Dict[str, Any],
        response_time_ms: float = 0.0,
        user_id: Optional[str] = None,
        session_id: Optional[str] = None,
    ) -> None:
        """
        Fire-and-forget: score + persist in a daemon thread.
        Never blocks the caller.
        """
        if not self._enabled:
            return
        thread = threading.Thread(
            target=self._score_and_save,
            args=(pipeline_result, response_time_ms, user_id, session_id),
            daemon=True,
        )
        thread.start()

    def get_stats(self, days: int = 7) -> Dict[str, Any]:
        """Return aggregate benchmark stats for the last N days."""
        if not self._supabase:
            return {}
        try:
            from datetime import timedelta
            since = (datetime.now(timezone.utc) - timedelta(days=days)).isoformat()
            rows = (
                self._supabase.table(self._table)
                .select("*")
                .gte("created_at", since)
                .order("created_at", desc=True)
                .limit(1000)
                .execute()
            ).data or []

            if not rows:
                return {"total_queries": 0, "period_days": days}

            total = len(rows)
            passed = sum(1 for r in rows if r.get("passed"))
            avg_score = sum(r.get("total_score", 0) for r in rows) / total
            avg_rt = sum(r.get("response_time_ms", 0) for r in rows) / total

            by_cat: Dict[str, Dict] = {}
            for r in rows:
                cat = r.get("category", "unknown")
                if cat not in by_cat:
                    by_cat[cat] = {"count": 0, "passed": 0, "total_score": 0}
                by_cat[cat]["count"] += 1
                by_cat[cat]["passed"] += int(r.get("passed", False))
                by_cat[cat]["total_score"] += r.get("total_score", 0)

            category_breakdown = {
                cat: {
                    "count": v["count"],
                    "pass_rate": round(v["passed"] / v["count"] * 100, 1),
                    "avg_score": round(v["total_score"] / v["count"], 1),
                }
                for cat, v in by_cat.items()
            }

            # Worst 5 queries
            worst = sorted(rows, key=lambda r: r.get("total_score", 0))[:5]
            worst_queries = [
                {
                    "query": r.get("query", ""),
                    "score": r.get("total_score", 0),
                    "category": r.get("category", ""),
                    "created_at": r.get("created_at", ""),
                }
                for r in worst
            ]

            # Score dimension averages
            dims = ["route_score", "verdict_score", "no_hedge_score",
                    "format_score", "pakistan_score"]
            dim_avgs = {
                d: round(sum(r.get(d, 0) for r in rows) / total, 1)
                for d in dims
            }

            return {
                "period_days": days,
                "total_queries": total,
                "pass_rate_pct": round(passed / total * 100, 1),
                "avg_score": round(avg_score, 1),
                "avg_response_time_ms": round(avg_rt, 1),
                "score_dimensions": dim_avgs,
                "category_breakdown": category_breakdown,
                "worst_queries": worst_queries,
                "recent": [
                    {
                        "query": r.get("query", "")[:80],
                        "score": r.get("total_score", 0),
                        "passed": r.get("passed", False),
                        "category": r.get("category", ""),
                        "created_at": r.get("created_at", ""),
                    }
                    for r in rows[:20]
                ],
            }
        except Exception as e:
            print(f"[Benchmark] get_stats failed: {e}")
            return {"error": str(e)}

    def get_recent(self, limit: int = 50) -> List[Dict[str, Any]]:
        """Return the N most recent benchmark result rows."""
        if not self._supabase:
            return []
        try:
            rows = (
                self._supabase.table(self._table)
                .select(
                    "query_id, query, category, is_prediction, total_score, "
                    "passed, route_score, verdict_score, no_hedge_score, "
                    "format_score, pakistan_score, response_time_ms, "
                    "source_label, created_at"
                )
                .order("created_at", desc=True)
                .limit(limit)
                .execute()
            ).data or []
            return rows
        except Exception as e:
            print(f"[Benchmark] get_recent failed: {e}")
            return []

    # ─────────────────────────────────────────────────────────────────────
    # INTERNAL
    # ─────────────────────────────────────────────────────────────────────

    def _score_and_save(
        self,
        pipeline_result: Dict[str, Any],
        response_time_ms: float,
        user_id: Optional[str],
        session_id: Optional[str],
    ) -> None:
        try:
            scores = _score_response(pipeline_result)

            answer_obj = pipeline_result.get("answer", {})
            answer_text = (
                answer_obj.get("answer", "")
                if isinstance(answer_obj, dict)
                else str(answer_obj)
            )

            row = {
                "query_id":        str(uuid.uuid4()),
                "session_id":      session_id or pipeline_result.get("conversation_id"),
                "user_id":         user_id or pipeline_result.get("user_id"),
                "query":           pipeline_result.get("query", ""),
                "category":        pipeline_result.get("category", "unknown"),
                "subcategory":     pipeline_result.get("subcategory", ""),
                "is_prediction":   bool(pipeline_result.get("is_prediction", False)),
                "actual_route":    pipeline_result.get("category", "unknown"),
                "answer_preview":  answer_text[:500],
                "response_time_ms": round(response_time_ms, 1),
                "route_score":     scores["route_score"],
                "verdict_score":   scores["verdict_score"],
                "no_hedge_score":  scores["no_hedge_score"],
                "format_score":    scores["format_score"],
                "pakistan_score":  scores["pakistan_score"],
                "total_score":     scores["total_score"],
                "passed":          scores["passed"],
                "source_label":    pipeline_result.get("source", ""),
                "accuracy_min":    pipeline_result.get("accuracy_min"),
                "accuracy_max":    pipeline_result.get("accuracy_max"),
            }

            with self._lock:
                self._supabase.table(self._table).insert(row).execute()
            print(
                f"[Benchmark] Saved: score={scores['total_score']}/100 "
                f"passed={scores['passed']} cat={row['category']} "
                f"rt={response_time_ms:.0f}ms"
            )
        except Exception as e:
            print(f"[Benchmark] Failed to save result: {e}")


# ─────────────────────────────────────────────────────────────────────────────
# MODULE-LEVEL SINGLETON  (shared across the process)
# ─────────────────────────────────────────────────────────────────────────────

_tracker: Optional[LiveBenchmarkTracker] = None


def get_tracker() -> LiveBenchmarkTracker:
    global _tracker
    if _tracker is None:
        _tracker = LiveBenchmarkTracker()
    return _tracker
