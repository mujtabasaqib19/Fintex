-- Live Benchmark Results Table
-- Run this once in your Supabase SQL editor to enable live benchmarking.

CREATE TABLE IF NOT EXISTS public.benchmark_results (
  id               bigint generated by default as identity primary key,
  query_id         text        not null,
  session_id       text,
  user_id          text,
  query            text        not null,
  category         text,
  subcategory      text,
  is_prediction    boolean     default false,
  actual_route     text,
  answer_preview   text,
  response_time_ms float,

  -- Score dimensions (each 0 or 20)
  route_score      int         default 0,
  verdict_score    int         default 0,
  no_hedge_score   int         default 0,
  format_score     int         default 0,
  pakistan_score   int         default 0,

  -- Composite
  total_score      int         default 0,
  passed           boolean     default false,

  -- Pipeline metadata
  source_label     text,
  accuracy_min     int,
  accuracy_max     int,

  created_at       timestamp with time zone
                     default timezone('utc'::text, now()) not null
);

-- Indexes for fast dashboard queries
CREATE INDEX IF NOT EXISTS idx_benchmark_created
  ON public.benchmark_results (created_at DESC);

CREATE INDEX IF NOT EXISTS idx_benchmark_category
  ON public.benchmark_results (category);

CREATE INDEX IF NOT EXISTS idx_benchmark_passed
  ON public.benchmark_results (passed, created_at DESC);

-- Useful view: per-day quality summary
CREATE OR REPLACE VIEW public.benchmark_daily AS
SELECT
  DATE(created_at)                                    AS day,
  COUNT(*)                                            AS total_queries,
  ROUND(AVG(total_score), 1)                          AS avg_score,
  ROUND(100.0 * SUM(CASE WHEN passed THEN 1 ELSE 0 END) / COUNT(*), 1) AS pass_rate_pct,
  ROUND(AVG(response_time_ms), 0)                     AS avg_response_time_ms,
  ROUND(AVG(route_score), 1)                          AS avg_route,
  ROUND(AVG(verdict_score), 1)                        AS avg_verdict,
  ROUND(AVG(no_hedge_score), 1)                       AS avg_no_hedge,
  ROUND(AVG(format_score), 1)                         AS avg_format,
  ROUND(AVG(pakistan_score), 1)                       AS avg_pakistan
FROM public.benchmark_results
GROUP BY DATE(created_at)
ORDER BY day DESC;
